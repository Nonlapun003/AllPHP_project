<?php

$alphabet=array("00","33","66","99","CC","FF");
$color=array("FF","CC","99","66","33","00");
sort($alphabet);
$num = count($alphabet);


echo "<table>";


for($i=0;$i<$num;$i++){
	echo '<tr>';

	
	for($j=0;$j<$num;$j++){
		echo '<tr>';
		for($k = 0 ; $k<$num ;$k++){
			
			echo "<td bgcolor = $alphabet[$i].$alphabet[$j].$alphabet[$k]>";
			echo "<font size = 5 color = $color[$i].$color[$j].$color[$k] > #$alphabet[$i]$alphabet[$j]$alphabet[$k] </font>";
			
			
			
			echo '</td>';
			

		}
		echo '</tr>';
	}

	echo '<tr>';

}

echo "</table>";



?>